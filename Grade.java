class Grade
{
public static void main(String args[])
{
int a=67;
if(a>=81){
System.out.println("A Grade");
}
else if(a<=80 && a>=71){
System.out.println("B Grade");
}
else if (a<=70 && a>=61){
System.out.println("C Grade");
}
else if(a<=60 && a>=40){
System.out.println("D Grade");
}
else if(a<40){
System.out.println("Failed");
}
}}
